module ModelHelper
  def remove_unicode text
    text = text.gsub(/đ|Đ/, "d")
      .gsub(/ị|í|ì|ỉ|ĩ|Ị|Í|Ỉ|Ĩ/, "i")
      .gsub(/ỵ|ý|ỳ|ỷ|ỹ|Ỵ|Ý|Ỳ|Ỷ|Ỹ/, "y")
      .gsub(/ẹ|é|è|ẻ|ẽ|Ẹ|É|È|Ẻ|É|ê|ệ|ế|ề|ể|ễ|Ê|Ệ|Ế|Ề|Ể|Ễ/, "e")
      .gsub(/ụ|ú|ù|ủ|ũ|Ụ|Ú|Ù|Ủ|Ũ|ư|ự|ữ|ừ|ự|ữ|Ư|Ự|Ứ|Ừ|Ử|Ữ/, "u")
      .gsub(/ạ|á|à|ả|ã|Ạ|Á|À|Ả|Ã|ă|ặ|ắ|ằ|ẳ|ẵ|Ă|Ặ|Ắ|Ằ|Ẳ|Ẵ|â|ậ|ấ|ầ|ẩ|ẫ|Â|Ậ|Ấ|Ầ|Ẩ|Ẫ/, "a")
      .gsub(/ọ|ó|ò|ỏ|õ|Ọ|Ó|Ò|Ỏ|Õ|ô|ộ|ố|ồ|ổ|ỗ|Ô|Ộ|Ỗ|Ồ|Ổ|Ỗ|ơ|ợ|ớ|ờ|ở|ỡ|Ơ|Ợ|Ớ|Ờ|Ở|Ỡ/, "o")
    return text.downcase
  end
end
